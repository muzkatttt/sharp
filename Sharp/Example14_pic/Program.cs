void PrintImage(int[,] pic) // метод выводит на консоль массив
{
    for (int i = 0; i < pic.GetLength(0); i++)
    {
        for (int j = 0; j < pic.GetLength(1); j++)
        {
            if (pic[i, j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

PrintImage(pic);

void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row - 1, col); // рекурсия!!!!!
        FillImage(row, col - 1);
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}
